<h1>Pagination</h1>

<p>
  This library offers two different types of pagination.
</p>

<h2>List-style pagination</h2>

<p>
  Pages are represented by a list of page numbers. The first and last page
  number are always visible. The current page number is also visible, along with
  some page numbers that come before and after it. Users can also use previous
  and next buttons to quickly navigate.
</p>

